#Bitwise opoerators
x = y =5
z = 2
print("x = ", x,
    "\ny = ", y,
    "\nz = ", z)

print("x & z : ", x & z)        #bitwise AND
print("x | z : ", x | z)        #bitwise OR
print("x ^ y : ", x ^ y)        #bitwise XOR
print("x << z : ", x << z)       #left shift 
print("x >> z : ", x >> z)       #right shift 
print("~x : ", ~x)              # bitwise NOT