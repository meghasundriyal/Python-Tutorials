#basic arithmetic operations
x  = 5                             #assignment operator
y = 4
print("x = ", x)   
print("y = ", y)                
print("x + y = ", x+y)             #add
print("x / y = ", x/y)             #float division 
print("x // y = ", x//y)           #floor division
print("x % y = ", x%y)             #modulus
print("x ** y = ", x**y)           #exponential         

